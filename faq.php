                            <div class="panel-heading">
                                <a data-toggle="collapse" data-parent="#accordion-cat-1" href="#faq-cat-1-sub-5">
                                    <h4 class="panel-title">
                                        What can I do to improve battery life?
                                        <span class="pull-right"><i class="glyphicon glyphicon-plus"></i></span>
                                    </h4>
                                </a>
                            </div>
                            <div id="faq-cat-1-sub-5" class="panel-collapse collapse">
                                <div class="panel-body">
                                    There are a few things you can do to keep your battery from losing its charge quickly (note that these methods will not increase the maximum charge). <br/><br/>1) Turn off WiFi.<br/>When not in use, WiFi has no use -- it simply wastes power. If you turn off your WiFi, you may notice your battery life increasing. <Br/><br/>2) Turn of Bluetooth.<br/>Though a useful feature, Bluetooth, like WiFi, serves no purpose when not being used. If you turn off your Bluetooth (assuming your computer has Bluetooth), your battery life may improve. <br/><br/>3) Close programs which are not in use.<br/>Not only do they slow down your computer, but programs that aren't in use just make your computer's processor (the CPU) do more work, which means it needs more power. Closing programs that aren't in use will improve battery life.<br/><br/>4) Do <b>not</b> disable a CPU core.<br/>Some computers have multiple CPU cores, which is a nice feature. This can speed up your computer and make it more powerful. Some people may assume that powering multiple cores will draw more power than only powering one, which seems logical. However, this is incorrect. Computers with multiple CPUs are designed to work more efficiently with all cores enabled, so disabling one will decrease the efficiency of the CPU. This will not only not improve battery life, but it may actually hurt it.</p>
</div>
