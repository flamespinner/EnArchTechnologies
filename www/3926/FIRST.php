<p><i>FIRST</i> was founded in 1989 by Dean Kamen and has grown to encompass hundreds of thousands of students of all ages. As of this year, there are about 293,000 students on 26,800 teams that create over 24,000 robots. Helping these students achieve their goals are over 101,000 volunteers who help to mentor and make <i>FIRST</i> what it is today.</p>
